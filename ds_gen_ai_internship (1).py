#capitalize code
def word_capitalize(text):
    if text == "":
        return ""
    result = ""
    first = text[0]
    if 'a' <= first <= 'z':
        result += chr(ord(first)-32)
    else:
        result += first
    for ch in text[1:]:
        if 'A' <= ch <= 'Z':
            result += chr(ord(ch)+32)
        else:
            result += ch
    return result


word_capitalize("heLLo")

