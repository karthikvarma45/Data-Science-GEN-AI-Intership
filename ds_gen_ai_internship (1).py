#Title
def my_title(text):
    result = ""
    nw = True
    for ch in text:
        if ch == " ":
            nw = True
            result += ch
        else:
            if nw and 'a' <= ch <= 'z':
                result += chr(ord(ch)-32)
            elif not nw and 'A' <= ch <= 'Z':
                result += chr(ord(ch)+32)
            else:
                result += ch
            nw = False
    return result
my_title("hello world program")

